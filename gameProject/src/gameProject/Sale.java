package gameProject;

public class Sale implements Entity {
	
	String name;
	int price;
	
	public Sale() {}
	
	public Sale(String name, int price) {
		super();
		this.name = name;
		this.price = price;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	

}
